package com.overklassniy.stankinschedule.schedule.table.ui.components

/**
 * Тип операции экспорта.
 *
 * Назначение: определяет, что делать с сформированным файлом.
 *
 * Возможные значения:
 * - Send — открыть системное диалоговое окно отправки
 * - Save — сохранить файл в выбранное пользователем место
 */
enum class ExportType {
    /** Отправка файла через системный шэринг. */
    Send,

    /** Сохранение файла по указанному пути. */
    Save;
}